// 머쓱이는 할머니께 생신 축하 편지를 쓰려고 합니다. 할머니가 보시기 편하도록 글자 한 자 한 자를 가로 2cm 크기로 적으려고 하며, 편지를 가로로만 적을 때, 축하 문구 message를 적기 위해 필요한 편지지의 최소 가로길이를 return 하도록 solution 함수를 완성해주세요.

// 제한사항
// 공백도 하나의 문자로 취급합니다.
// 1 ≤ message의 길이 ≤ 50
// 편지지의 여백은 생각하지 않습니다.
// message는 영문 알파벳 대소문자, ‘!’, ‘~’ 또는 공백으로만 이루어져 있습니다.
// 입출력 예
// message	result
// "happy birthday!"	30
// "I love you~"	22
// 입출력 예 설명
// 입출력 예 #1

// message의 글자 수가 15개로 최소 가로 30cm의 편지지가 필요합니다.
// 입출력 예 #2

// message의 글자 수가 11개로 최소 가로 22cm의 편지지가 필요합니다.

// ------------------------------------------------------------------------------------------

// 나의 풀이

function solution(message) {
  var answer = [...message].length * 2;
  return answer;
}

// ------------------------------------------------------------------------------------------

// 다른 사람의 풀이

{
  function solution(message) {
    // 문자열도 length를 사용할 수 있음!
    return message.length * 2;
  }
}

{
  function solution(message) {
    // 내 풀이에서 스프레드 문법 말고 split()을 사용한 버전
    return message.split("").length * 2;
  }
}

// 댓글 중 "이모지나 복합 문자는 length가 다르게 계산될 우려가 있어서 스프레드 문법을 써서 더 안전하게 할수있어요.
// 대한 답변
// 댓글의 의견("이모지나 복합 문자는 length가 다르게 계산될 우려...")은 일반적인 자바스크립트 환경에서는 맞는 말이지만, 이 문제에서는 해당되지 않는 말입니다.
// 왜 그런 댓글이 달렸을까요 ? (유니코드 이슈)자바스크립트는 문자열을 UTF - 16 방식으로 저장합니다.
// - 일반 문자(영어, 숫자 등): 1개의 공간(Code Unit)을 차지 / length는 1
// - 이모지(🥑)나 특수 문자: 2개의 공간(Surrogate Pair)을 차지 / length는 2스프레드 문법(...)은 문자를 보이는 그대로(Code Point) 인식해서 배열로 만들기 때문에 이 문제를 해결해 줍니다.
// 예시 비교:| 코드 | 결과 | 설명
// - "AB".length | 2 | 일반 문자 2개
// -[..."AB"].length | 2 | 일반 문자 2개 || "🥑".length | 2 |
// 이모지는 2칸을 차지함(부정확할 수 있음) || [..."🥑"].length | 1 | 이모지 1개로 정확히 인식함

// 제한사항
// message는 영문 알파벳 대소문자, ‘!’, ‘~’ 또는 공백으로만 이루어져 있습니다.
// 이 문제에 등장하는 모든 문자(영어, 공백, !, ~)는 1개의 공간만 차지하는 기본 문자들입니다. 따라서 length 속성을 바로 사용해도 오차 없이 정확한 길이를 반환합니다.

// 효율성: 작성하신 코드(message.length * 2)가 더 효율적입니다. 불필요한 배열 생성을 하지 않기 때문입니다.

// 안전성: 이 문제의 입력 조건(영문, 특수기호 일부) 하에서는 length를 바로 써도 100% 안전합니다.

// 결론: 댓글의 조언은 "이모지가 포함된 일반적인 서비스 개발"에서는 꿀팁이지만, "이 코딩테스트 문제"에서는 과도한 처방(Over-engineering)입니다.
